// Expose a hook for carousel logic to know when language changed
window.onLanguageChange = null;

document.addEventListener('DOMContentLoaded', () => {

    // --- Language Toggle Logic ---
    const langToggleBtns = document.querySelectorAll('.lang-toggle'); // support multiple if needed
    let currentLang = localStorage.getItem('lang') || 'en';

    function setLanguage(lang) {
        currentLang = lang;
        localStorage.setItem('lang', lang);

        document.documentElement.setAttribute('lang', lang);
        document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
        
        langToggleBtns.forEach(btn => {
            btn.innerText = lang === 'ar' ? 'En' : 'عربي';
        });

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
            
            // Adjust form placeholders
            if(el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (translations[lang] && translations[lang][key]) {
                    el.placeholder = translations[lang][key];
                }
            }
        });

        if(window.onLanguageChange) {
            window.onLanguageChange();
        }
    }

    // Initialize Language
    setLanguage(currentLang);

    langToggleBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(currentLang === 'en' ? 'ar' : 'en');
        });
    });

    // --- Success Stories Carousel Logic ---
    const succTrack = document.getElementById('succ-track');
    const succNext = document.getElementById('succ-next');
    const succPrev = document.getElementById('succ-prev');

    if (succTrack && succNext && succPrev) {
        let currentIndex = 0;
        const slides = succTrack.querySelectorAll('.carousel-slide');
        const numSlides = slides.length;

        function updateCarouselPosition() {
            const isRTL = document.documentElement.dir === 'rtl';
            const multiplier = isRTL ? 1 : -1;
            succTrack.style.transform = `translateX(${currentIndex * 100 * multiplier}%)`;
        }

        succNext.addEventListener('click', () => {
             // In LTR, Next means moving right (increasing index)
             currentIndex = (currentIndex + 1) % numSlides;
             updateCarouselPosition();
        });

        succPrev.addEventListener('click', () => {
            // In LTR, Prev means moving left (decreasing index)
            currentIndex = (currentIndex - 1 + numSlides) % numSlides;
            updateCarouselPosition();
        });

        // Whenever language resets, reset carousel to first slide to rebuild layout properly
        window.onLanguageChange = () => {
            currentIndex = 0;
            updateCarouselPosition();
        };
        // Run once on load
        updateCarouselPosition();
    }

    // --- Mobile Menu Toggle ---
    const hamburger = document.getElementById('hamburger');
    const mobileMenu = document.getElementById('mobile-menu');
    const mobileLinks = document.querySelectorAll('.mobile-nav-link');
    
    if(hamburger && mobileMenu) {
        hamburger.addEventListener('click', () => {
            hamburger.classList.toggle('active');
            mobileMenu.classList.toggle('active');
            document.body.style.overflow = mobileMenu.classList.contains('active') ? 'hidden' : 'auto';
        });
        
        mobileLinks.forEach(link => {
            link.addEventListener('click', () => {
                hamburger.classList.remove('active');
                mobileMenu.classList.remove('active');
                document.body.style.overflow = 'auto';
            });
        });
    }

    // --- Sticky Header Background on Scroll ---
    const navbar = document.getElementById('navbar');
    if(navbar) {
        window.addEventListener('scroll', () => {
            if (window.scrollY > 50) {
                navbar.style.boxShadow = '0 4px 20px rgba(0, 0, 0, 0.1)';
            } else {
                navbar.style.boxShadow = 'none';
            }
        });
    }

    // --- Dark/Light Mode Theme Toggle ---
    const themeToggle = document.getElementById('theme-toggle');
    if (themeToggle) {
        const themeIcon = themeToggle.querySelector('i');
        const body = document.body;
        
        const savedTheme = localStorage.getItem('theme');
        if (savedTheme === 'light') {
            body.classList.replace('dark-mode', 'light-mode');
            if(themeIcon) themeIcon.classList.replace('fa-moon', 'fa-sun');
        }

        themeToggle.addEventListener('click', () => {
            if (body.classList.contains('dark-mode')) {
                body.classList.replace('dark-mode', 'light-mode');
                if(themeIcon) themeIcon.classList.replace('fa-moon', 'fa-sun');
                localStorage.setItem('theme', 'light');
            } else {
                body.classList.replace('light-mode', 'dark-mode');
                if(themeIcon) themeIcon.classList.replace('fa-sun', 'fa-moon');
                localStorage.setItem('theme', 'dark');
            }
        });
    }

    // --- Intersection Observer for Scroll Animations ---
    const animatedElements = document.querySelectorAll('.animate-on-scroll');
    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.15
    };

    if('IntersectionObserver' in window && animatedElements.length > 0) {
        const observer = new IntersectionObserver((entries, observer) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('is-visible');
                }
            });
        }, observerOptions);

        animatedElements.forEach(el => observer.observe(el));
    } else {
        // Fallback for sub-pages or unsupported browsers
        animatedElements.forEach(el => el.classList.add('is-visible'));
    }

    // --- Smooth Scrolling for anchor links ---
    // Only smooth scroll for hash links on the SAME page
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            const targetId = this.getAttribute('href');
            if(targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if(targetElement) {
                e.preventDefault();
                const navHeight = document.querySelector('.navbar') ? document.querySelector('.navbar').offsetHeight : 0;
                const targetPosition = targetElement.getBoundingClientRect().top + window.scrollY - navHeight;
                
                window.scrollTo({
                    top: targetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });

    // --- Basic Form Submission Prevent ---
    const contactForm = document.getElementById('contactForm');
    if(contactForm) {
        contactForm.addEventListener('submit', (e) => {
            e.preventDefault();
            const btn = contactForm.querySelector('button[type="submit"]');
            const originalText = btn.innerHTML;
            
            btn.innerHTML = currentLang === 'ar' ? '<i class="fa-solid fa-check"></i> تم إرسال الرسالة!' : '<i class="fa-solid fa-check"></i> Message Sent!';
            
            btn.style.backgroundColor = 'var(--accent-secondary)';
            contactForm.reset();
            
            setTimeout(() => {
                btn.innerHTML = translations[currentLang]['form_btn'];
                btn.style.backgroundColor = '';
            }, 3000);
        });
    }
});
